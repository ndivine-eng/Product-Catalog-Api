const mongoose = require('mongoose');

const categorySchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  description: {
    type: String,
    trim: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

// âœ… Prevent OverwriteModelError during development
module.exports = mongoose.models.Category || mongoose.model('Category', categorySchema);
